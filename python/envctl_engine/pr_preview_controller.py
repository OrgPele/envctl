def public_route_repo_name(repo_slug: str) -> str:
    repo_name = str(repo_slug or "").strip().rstrip("/").rsplit("/", 1)[-1]
    chars: list[str] = []
    previous_dash = False
    for char in repo_name.lower():
        if char.isalnum():
            chars.append(char)
            previous_dash = False
        elif not previous_dash:
            chars.append("-")
            previous_dash = True
    return "".join(chars).strip("-") or "repo"


def public_route_host(
    pr_number: int,
    role: str,
    base_domain: str,
    repo_slug: str,
) -> str:
    prefix = f"{public_route_repo_name(repo_slug)}-pr-{pr_number}"
    if role == "backend":
        prefix = f"{prefix}-api"
    elif role == "supabase":
        prefix = f"{prefix}-supabase"
    elif role != "frontend":
        prefix = f"{prefix}-{docker_name_fragment(role)}"
    return f"{prefix}.{base_domain.strip('.')}"



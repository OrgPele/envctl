        if self.config.public_base_domain:
            lines.append(f"- Public base domain: `{self.config.public_base_domain}`")
        if self.config.public_link_token_configured:
            lines.append("- Public link token: `configured`")
        return "".join(f"{line}\n" for line in lines)


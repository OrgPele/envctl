def comma_separated_config_names(
    config_text: str,
    key: str,
    *,
    pattern: re.Pattern[str],
) -> tuple[str, ...]:
    raw = parse_envctl_text(config_text).get(key, "")
    names: list[str] = []
    for item in raw.split(","):
        name = item.strip()
        if not name:
            continue
        if not pattern.fullmatch(name):
            raise ValueError(f"{key} contains invalid name {name!r}")
        if name not in names:
            names.append(name)
    return tuple(names)



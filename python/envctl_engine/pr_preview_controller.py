    def try_envctl_json(self, args: list[str]) -> dict[str, Any]:
        result = self.runner.run(
            [self.config.envctl_bin, *args],
            cwd=self.config.control_repo,
            check=False,
            env=self.envctl_command_env(),
        )
        if result.returncode != 0:
            return {
                "ok": False,
                "error": truncate(result.stderr or result.stdout),
            }
        try:
            data = json.loads(result.stdout)
        except json.JSONDecodeError:
            return {"ok": False, "error": "envctl returned non-JSON output"}
        return data if isinstance(data, dict) else {"value": data}

    def external_dependency_leases_path(self) -> Path:
        return self.config.state_dir / "external-dependency-leases.json"

    def load_external_dependency_leases(self) -> dict[str, dict[str, int]]:
        path = self.external_dependency_leases_path()
        if not path.exists():
            return {}
        payload = json.loads(path.read_text())
        if not isinstance(payload, dict):
            return {}
        leases: dict[str, dict[str, int]] = {}
        for dependency, slots in payload.items():
            if not isinstance(slots, dict):
                continue
            dep_name = normalize_external_dependency_name(str(dependency))
            leases[dep_name] = {}
            for slot_id, owner in slots.items():
                try:
                    leases[dep_name][str(slot_id)] = int(owner)
                except (TypeError, ValueError):
                    continue
        return leases

    def save_external_dependency_leases(
        self,
        leases: dict[str, dict[str, int]],
    ) -> None:
        self.config.state_dir.mkdir(parents=True, exist_ok=True)
        cleaned = {
            dep_name: {slot_id: owner for slot_id, owner in sorted(slots.items()) if owner > 0}
            for dep_name, slots in sorted(leases.items())
        }
        cleaned = {dep_name: slots for dep_name, slots in cleaned.items() if slots}
        path = self.external_dependency_leases_path()
        tmp = path.with_suffix(".tmp")
        tmp.write_text(json.dumps(cleaned, indent=2, sort_keys=True))
        tmp.replace(path)

    def acquire_external_dependency_leases(
        self,
        pr: PullRequestInfo,
        existing_state: PreviewState | None,
    ) -> dict[str, str]:
        if not self.config.external_dependency_pools:
            return {}
        leases = self.load_external_dependency_leases()
        current = existing_state.external_dependencies if existing_state else {}
        acquired: dict[str, str] = {}
        changed = False
        for dep_name, slots in self.config.external_dependency_pools.items():
            if not slots:
                continue
            dep_leases = leases.setdefault(dep_name, {})
            preferred_slot = current.get(dep_name)
            if preferred_slot and external_dependency_slot(
                self.config.external_dependency_pools,
                dep_name,
                preferred_slot,
            ):
                owner = dep_leases.get(preferred_slot)
                if owner in (None, pr.number):
                    if owner != pr.number:
                        dep_leases[preferred_slot] = pr.number
                        changed = True
                    acquired[dep_name] = preferred_slot
                    continue
            for slot in slots:
                owner = dep_leases.get(slot.slot_id)
                if owner not in (None, pr.number):
                    continue
                if owner != pr.number:
                    dep_leases[slot.slot_id] = pr.number
                    changed = True
                acquired[dep_name] = slot.slot_id
                break
        if changed:
            self.save_external_dependency_leases(leases)
        return acquired

    def release_external_dependency_leases(
        self,
        pr_number: int,
        external_dependencies: dict[str, str] | None = None,
    ) -> None:
        leases = self.load_external_dependency_leases()
        changed = False
        for dep_name, dep_leases in list(leases.items()):
            target_slot = None
            if external_dependencies is not None:
                target_slot = external_dependencies.get(dep_name)
                if target_slot is None:
                    continue
            for slot_id, owner in list(dep_leases.items()):
                if owner != pr_number:
                    continue
                if target_slot is not None and slot_id != target_slot:
                    continue
                dep_leases.pop(slot_id, None)
                changed = True
            if not dep_leases:
                leases.pop(dep_name, None)
        if changed:
            self.save_external_dependency_leases(leases)

    def external_dependency_start_env(
        self,
        external_dependencies: dict[str, str],
    ) -> dict[str, str]:
        env: dict[str, str] = {}
        for dep_name, slot_id in external_dependencies.items():
            slot = external_dependency_slot(
                self.config.external_dependency_pools,
                dep_name,
                slot_id,
            )
            if not slot:
                continue
            for key, value in slot.backend_env.items():
                env[f"ENVCTL_BACKEND_ENV__{key}"] = value
            for key, value in slot.frontend_env.items():
                env[f"ENVCTL_FRONTEND_ENV__{key}"] = value
        return env


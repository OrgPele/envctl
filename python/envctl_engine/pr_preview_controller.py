    def create_deployment(self, pr: PullRequestInfo, state: PreviewState) -> str | None:
        payload = {
            "ref": pr.head_sha or pr.head_ref,
            "environment": public_route_environment(self.config.repo_slug, pr.number),
            "description": f"Envctl PR preview for PR #{pr.number}",
            "auto_merge": False,
            "required_contexts": [],
            "transient_environment": True,
            "production_environment": False,
        }
        result = self.runner.run(
            [
                "gh",
                "api",
                "-X",
                "POST",
                f"repos/{self.config.repo_slug}/deployments",
                "--input",
                "-",
                "--jq",
                ".id",
            ],
            input_text=json.dumps(payload),
            check=False,
        )
        if result.returncode != 0:
            print(f"Failed to create GitHub deployment: {result.stderr}")
            return None
        deployment_id = result.stdout.strip()
        if not deployment_id:
            print("Failed to create GitHub deployment: response did not include an id")
            return None

        status_payload: dict[str, Any] = {
            "state": "success",
            "description": "Envctl preview is running",
            "auto_inactive": False,
        }
        deployment_url = self.preview_deployment_url(state)
        if deployment_url:
            status_payload["environment_url"] = deployment_url
        if pr.url:
            status_payload["log_url"] = pr.url
        status_result = self.runner.run(
            [
                "gh",
                "api",
                "-X",
                "POST",
                f"repos/{self.config.repo_slug}/deployments/{deployment_id}/statuses",
                "--input",
                "-",
            ],
            input_text=json.dumps(status_payload),
            check=False,
        )
        if status_result.returncode != 0:
            print(f"Failed to mark GitHub deployment successful: {status_result.stderr}")
        return deployment_id


def external_dependency_envctl_config_lines(
    external_dependencies: dict[str, str] | None,
    pools: dict[str, tuple[ExternalDependencySlot, ...]] | None,
) -> str:
    external_dependencies = external_dependencies or {}
    pools = pools or {}
    values = {
        "TREES_REDIS_ENABLE": "true",
        "TREES_SUPABASE_ENABLE": "true",
        "TREES_N8N_ENABLE": "true",
    }
    custom_values: dict[str, str] = {}
    for dep_name, slot_id in external_dependencies.items():
        config_key = MANAGED_TREE_DEP_CONFIG_KEYS.get(dep_name)
        if config_key:
            values[config_key] = "false"
        slot = external_dependency_slot(pools, dep_name, slot_id)
        if slot:
            custom_values.update(slot.envctl_config)
    lines = [
        f"TREES_REDIS_ENABLE={values['TREES_REDIS_ENABLE']}",
        f"TREES_SUPABASE_ENABLE={values['TREES_SUPABASE_ENABLE']}",
        f"TREES_N8N_ENABLE={values['TREES_N8N_ENABLE']}",
    ]
    for key in sorted(custom_values):
        lines.append(f"{key}={envctl_config_value(custom_values[key])}")
    return "\n".join(lines)


def external_dependency_slot(
    pools: dict[str, tuple[ExternalDependencySlot, ...]],
    dependency: str,
    slot_id: str,
) -> ExternalDependencySlot | None:
    for slot in pools.get(dependency, ()):
        if slot.slot_id == slot_id:
            return slot
    return None



def render_endpoint_lines(endpoints: dict[str, Any]) -> str:
    if not endpoints:
        return ""
    frontend = ((endpoints.get("frontend") or {}).get("public_url")) or (
        (endpoints.get("frontend") or {}).get("local_url")
    )
    backend = ((endpoints.get("backend") or {}).get("public_url")) or (
        (endpoints.get("backend") or {}).get("local_url")
    )
    supabase = ((endpoints.get("supabase") or {}).get("public_url")) or (
        (endpoints.get("supabase") or {}).get("local_url")
    )
    lines: list[str] = []
    if frontend:
        lines.append(f"- Frontend: {frontend}")
    if backend:
        lines.append(f"- Backend: {backend}")
    if supabase:
        lines.append(f"- Supabase: {supabase}")
    for name, payload in sorted((endpoints.get("additional_services") or {}).items()):
        if not isinstance(payload, dict):
            continue
        url = payload.get("public_url") or payload.get("local_url")
        if url:
            label = name.replace("-", " ").title()
            lines.append(f"- {label}: {url}")
    if not lines and endpoints.get("error"):
        lines.append(f"- Endpoint lookup: `{endpoints['error']}`")
    return "\n" + "\n".join(lines) + "\n" if lines else ""


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--event-path")
    parser.add_argument("--event-name")
    parser.add_argument("--repo")
    parser.add_argument("--label")
    parser.add_argument("--ttl-minutes", type=int)
    parser.add_argument("--command", choices=["start", "stop", "delete", "sweep"])
    parser.add_argument("--pr-number", type=int)
    parser.add_argument("--dry-run", action="store_true")
    return parser


def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    config = ControllerConfig.from_env(args)
    controller = PreviewController(config, CommandRunner(dry_run=config.dry_run))
    return controller.run(args)


def run_pr_preview_controller_command(_runtime: Any, route: Any) -> int:
    return main(list(getattr(route, "passthrough_args", ()) or ()))



    def render_external_dependency_mode(self, state: PreviewState) -> str:
        if not self.config.external_dependency_pools:
            return "`isolated` forced by workflow"
        parts: list[str] = []
        for dep_name in sorted(self.config.external_dependency_pools):
            slot_id = state.external_dependencies.get(dep_name)
            if slot_id:
                parts.append(f"{dep_name}=`{slot_id}`")
            else:
                parts.append(f"{dep_name}=`isolated`")
        summary = ", ".join(parts) or "none"
        return f"`isolated` by default; external leases: {summary}"

    def render_public_link_lines(self) -> str:
        lines: list[str] = []

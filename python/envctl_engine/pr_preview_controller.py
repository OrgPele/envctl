class CommandError(RuntimeError):
    def __init__(
        self,
        argv: list[str],
        cwd: Path | None,
        returncode: int,
        stdout: str,
        stderr: str,
    ):
        self.argv = argv
        self.cwd = cwd
        self.returncode = returncode
        self.stdout = stdout
        self.stderr = stderr
        super().__init__(f"command failed with exit {returncode}: {' '.join(argv)}")


@dataclass(frozen=True)
class CommandResult:
    argv: list[str]
    returncode: int
    stdout: str
    stderr: str


class CommandRunner:
    def __init__(self, *, dry_run: bool = False) -> None:
        self.dry_run = dry_run

    def run(
        self,
        argv: list[str],
        *,
        cwd: Path | None = None,
        check: bool = True,
        input_text: str | None = None,
        timeout: float | None = None,
        env: dict[str, str] | None = None,
        display_argv: list[str] | None = None,
    ) -> CommandResult:
        printable_cwd = str(cwd) if cwd else os.getcwd()
        printed_argv = display_argv or argv
        print(f"$ ({printable_cwd}) {' '.join(printed_argv)}", flush=True)
        if self.dry_run and dry_run_skips_command(argv):
            return CommandResult(argv=argv, returncode=0, stdout="", stderr="")
        try:
            proc = subprocess.run(
                argv,
                cwd=str(cwd) if cwd else None,
                input=input_text,
                text=True,
                capture_output=True,
                timeout=timeout,
                env=env,
            )
        except subprocess.TimeoutExpired as exc:
            stdout = decode_timeout_output(exc.stdout)
            stderr = decode_timeout_output(exc.stderr)
            if stderr:
                stderr = f"{stderr}\ncommand timed out after {timeout} seconds"
            else:
                stderr = f"command timed out after {timeout} seconds"
            result = CommandResult(
                argv=argv,
                returncode=124,
                stdout=stdout,
                stderr=stderr,
            )
            if check:
                raise CommandError(argv, cwd, result.returncode, stdout, stderr) from exc
            print(stderr, file=sys.stderr)
            return result
        except FileNotFoundError as exc:
            stderr = f"command not found: {argv[0]}"
            result = CommandResult(
                argv=argv,
                returncode=127,
                stdout="",
                stderr=stderr,
            )
            if check:
                raise CommandError(argv, cwd, result.returncode, "", stderr) from exc
            print(stderr, file=sys.stderr)
            return result
        if proc.stdout:
            print(proc.stdout[-8000:], end="" if proc.stdout.endswith("\n") else "\n")
        if proc.stderr:
            print(
                proc.stderr[-8000:],
                end="" if proc.stderr.endswith("\n") else "\n",
                file=sys.stderr,
            )
        result = CommandResult(
            argv=argv,
            returncode=proc.returncode,
            stdout=proc.stdout,
            stderr=proc.stderr,
        )
        if check and proc.returncode != 0:
            raise CommandError(argv, cwd, proc.returncode, proc.stdout, proc.stderr)
        return result

    def json(self, argv: list[str], *, cwd: Path | None = None) -> Any:
        result = self.run(argv, cwd=cwd)
        return json.loads(result.stdout or "null")


def dry_run_skips_command(argv: list[str]) -> bool:
    if not argv:
        return False
    if argv[0] == "git" or Path(argv[0]).name == "envctl":
        return True
    if argv[:3] == ["docker", "rm", "-f"] or argv[:2] == ["docker", "run"]:
        return True
    if argv[:3] in (
        ["gh", "auth", "setup-git"],
        ["gh", "issue", "comment"],
        ["gh", "issue", "edit"],
        ["gh", "repo", "clone"],
    ):
        return True
    return False


def validate_qa_user_email(email: str) -> str:
    value = email.strip()
    if not QA_USER_EMAIL_PATTERN.fullmatch(value):
        raise ValueError("ENVCTL_PREVIEW_QA_USER_EMAIL must render to a valid public email address")
    domain = value.rsplit("@", 1)[1].lower().rstrip(".")
    labels = domain.split(".")
    if domain in QA_USER_RESERVED_DOMAINS or labels[-1] in QA_USER_RESERVED_DOMAINS:
        raise ValueError("ENVCTL_PREVIEW_QA_USER_EMAIL must not use a reserved or local-only domain")
    return value


def render_qa_user_email(template: str, pr_number: int) -> str:
    try:
        email = template.format(pr_number=pr_number)
    except KeyError as exc:
        raise ValueError("ENVCTL_PREVIEW_QA_USER_EMAIL supports only the {pr_number} placeholder") from exc
    except ValueError as exc:
        raise ValueError("ENVCTL_PREVIEW_QA_USER_EMAIL contains an invalid format template") from exc
    return validate_qa_user_email(email)



def parse_head_repository(
    head_repository: Any,
    head_repository_owner: Any = None,
) -> tuple[str, str]:
    head_repo = head_repository if isinstance(head_repository, dict) else {}
    owner = head_repository_owner if isinstance(head_repository_owner, dict) else {}
    repo_name = str(head_repo.get("name") or "")
    owner_name = str(owner.get("login") or owner.get("name") or "")
    name_with_owner = str(head_repo.get("nameWithOwner") or "")
    if name_with_owner and (not owner_name or not repo_name):
        parsed_owner, _, parsed_repo = name_with_owner.partition("/")
        owner_name = owner_name or parsed_owner
        repo_name = repo_name or parsed_repo
    embedded_owner = head_repo.get("owner")
    if not owner_name and isinstance(embedded_owner, dict):
        owner_name = str(embedded_owner.get("login") or embedded_owner.get("name") or "")
    return repo_name, owner_name


def timeline_label_active_since(lines: Iterable[str], label: str) -> datetime | None:
    active_since: datetime | None = None
    for line in lines:
        parts = line.rstrip("\n").split("\t")
        if len(parts) < 3:
            continue
        event, created_at, event_label = parts[0], parts[1], parts[2]
        if event_label != label:
            continue
        if event == "labeled":
            active_since = parse_github_datetime(created_at)
        elif event == "unlabeled":
            active_since = None
    return active_since


def select_project_for_branch(
    projects: Iterable[dict[str, Any]],
    head_ref: str,
    branch_for_root: Callable[[str], str | None],
) -> dict[str, Any] | None:
    by_name: list[dict[str, Any]] = []
    by_branch: list[dict[str, Any]] = []
    for project in projects:
        if str(project.get("name") or "") == head_ref:
            by_name.append(project)
        root = project.get("root")
        if root and branch_for_root(str(root)) == head_ref:
            by_branch.append(project)
    candidates = by_name or by_branch
    if not candidates:
        return None
    running = [p for p in candidates if p.get("running") or p.get("services_running")]
    return (running or candidates)[0]


def collect_machine_stats(disk_path: Path, runner: CommandRunner) -> MachineStats:
    try:
        load_1m, load_5m, load_15m = os.getloadavg()
    except OSError:
        load_1m, load_5m, load_15m = 0.0, 0.0, 0.0
    cpu_count = os.cpu_count() or 1
    memory_total, memory_available = read_memory()
    disk_target = disk_path if disk_path.exists() else Path("/")
    disk = shutil.disk_usage(disk_target)
    docker_count: int | None = None
    docker_error: str | None = None
    docker = runner.run(
        ["docker", "ps", "--format", "{{.Names}}"],
        check=False,
        timeout=5,
    )
    if docker.returncode == 0:
        docker_count = len([line for line in docker.stdout.splitlines() if line.strip()])
    else:
        docker_error = truncate(docker.stderr.strip() or docker.stdout.strip(), 300)
    top_processes = tuple(read_top_processes())
    return MachineStats(
        load_1m=float(load_1m),
        load_5m=float(load_5m),
        load_15m=float(load_15m),
        cpu_count=cpu_count,
        memory_total_bytes=memory_total,
        memory_available_bytes=memory_available,
        disk_total_bytes=int(disk.total),
        disk_free_bytes=int(disk.free),
        docker_running_containers=docker_count,
        docker_error=docker_error,
        top_processes=top_processes,
    )


def read_memory() -> tuple[int | None, int | None]:
    linux = read_linux_memory()
    if linux != (None, None):
        return linux
    return read_macos_memory()


def read_linux_memory() -> tuple[int | None, int | None]:
    meminfo = Path("/proc/meminfo")
    if not meminfo.exists():
        return None, None
    values: dict[str, int] = {}
    for line in meminfo.read_text().splitlines():
        if ":" not in line:
            continue
        key, rest = line.split(":", 1)
        fields = rest.strip().split()
        if not fields:
            continue
        try:
            values[key] = int(fields[0]) * 1024
        except ValueError:
            continue
    return values.get("MemTotal"), values.get("MemAvailable")


def read_macos_memory() -> tuple[int | None, int | None]:
    total_result = run_capture(["sysctl", "-n", "hw.memsize"], timeout=5)
    vm_stat_result = run_capture(["vm_stat"], timeout=5)
    if total_result is None or vm_stat_result is None:
        return None, None
    try:
        total = int(total_result.stdout.strip())
    except ValueError:
        return None, None
    page_size = 4096
    values: dict[str, int] = {}
    for line in vm_stat_result.stdout.splitlines():
        if "page size of" in line:
            try:
                page_size = int(line.split("page size of", 1)[1].split()[0])
            except (IndexError, ValueError):
                page_size = 4096
            continue
        if ":" not in line:
            continue
        key, rest = line.split(":", 1)
        raw_value = rest.strip().rstrip(".")
        try:
            values[key.strip().lower()] = int(raw_value)
        except ValueError:
            continue
    available_pages = (
        values.get("pages free", 0)
        + values.get("pages inactive", 0)
        + values.get("pages speculative", 0)
        + values.get("pages purgeable", 0)
    )
    return total, available_pages * page_size


def read_top_processes() -> list[str]:
    for argv in (
        ["ps", "-eo", "pid,comm,pcpu,pmem", "--sort=-pcpu"],
        ["ps", "-axo", "pid,comm,%cpu,%mem", "-r"],
    ):
        result = run_capture(argv, timeout=5)
        if result is None:
            continue
        lines = [line.strip() for line in result.stdout.splitlines()[1:6]]
        lines = [line for line in lines if line]
        if lines:
            return lines
    return []


def run_capture(argv: list[str], *, timeout: float) -> subprocess.CompletedProcess[str] | None:
    try:
        result = subprocess.run(
            argv,
            text=True,
            capture_output=True,
            timeout=timeout,
        )
    except Exception:
        return None
    if result.returncode != 0:
        return None
    return result


def overload_reasons(
    stats: MachineStats,
    active_prs: list[PullRequestInfo],
    current_pr_number: int,
    config: ControllerConfig,
) -> list[str]:
    reasons: list[str] = []
    if stats.load_per_cpu > config.max_load_per_cpu:
        reasons.append(
            f"CPU load pressure is {stats.load_per_cpu_percent:.1f}% "
            f"(1m load per CPU is {stats.load_per_cpu:.2f}), "
            f"above threshold {config.max_load_per_cpu:.2f}"
        )
    if (
        stats.memory_available_percent is not None
        and stats.memory_available_percent < config.min_memory_available_percent
    ):
        reasons.append(
            f"available memory is {stats.memory_available_percent:.1f}%, "
            f"below threshold {config.min_memory_available_percent:.1f}%"
        )
    if stats.disk_free_percent < config.min_disk_free_percent:
        reasons.append(
            f"free disk is {stats.disk_free_percent:.1f}%, below threshold {config.min_disk_free_percent:.1f}%"
        )
    other_active = [pr for pr in active_prs if pr.number != current_pr_number]
    if len(other_active) >= config.max_other_active_previews:
        reasons.append(
            f"{len(other_active)} other envctl previews are already active, "
            f"meeting limit {config.max_other_active_previews}"
        )
    return reasons



    def remove_wrong_branch_import_target(self, result: CommandResult) -> bool:
        target = import_reuse_failure_target(result)
        if target is None:
            return False
        try:
            resolved_target = target.expanduser().resolve(strict=False)
            imported_root = (self.config.control_repo / "trees" / "imported").resolve(strict=False)
        except OSError:
            return False
        if resolved_target != imported_root and imported_root not in (resolved_target.parents):
            print(
                f"Refusing to remove wrong-branch import target outside {imported_root}: {resolved_target}",
                flush=True,
            )
            return False

        remove_result = self.runner.run(
            [
                "git",
                "-C",
                str(self.config.control_repo),
                "worktree",
                "remove",
                "--force",
                str(resolved_target),
            ],
            cwd=self.config.control_repo,
            check=False,
            env=self.envctl_command_env(keep_github_tokens=True),
        )
        if remove_result.returncode == 0:
            return True
        print(
            "git worktree remove failed for stale import target; falling "
            f"back to directory cleanup. stdout={truncate(remove_result.stdout)!r} "
            f"stderr={truncate(remove_result.stderr)!r}",
            flush=True,
        )
        shutil.rmtree(resolved_target, ignore_errors=True)
        return not resolved_target.exists()

    def cleanup_failed_start_preview(self, state: PreviewState) -> int:
        self.remove_public_routes(state.pr_number)
        cleanup_result = self.runner.run(
            [
                self.config.envctl_bin,
                "blast-worktree",
                "--project",
                state.project or "",
                "--yes",
            ],
            cwd=self.config.control_repo,
            check=False,
            env=self.envctl_command_env(),
        )
        missing_target = "No matching targets found" in (cleanup_result.stdout + cleanup_result.stderr)
        if cleanup_result.returncode != 0 and not missing_target:
            return cleanup_result.returncode
        for raw_name in (
            state.head_ref,
            state.project or "",
            Path(state.root).name if state.root else "",
        ):
            self.remove_project_docker_artifacts(raw_name)
        return 0

    def publish_public_routes(
        self,
        pr_number: int,
        endpoints: dict[str, Any],
    ) -> dict[str, Any]:
        if not self.public_routes_enabled():
            return endpoints
        updated = json.loads(json.dumps(endpoints))
        routes = self.public_route_urls(pr_number)
        public_services = tuple(
            service for service in self.preview_public_services() if endpoint_payload(endpoints, service)
        )
        for role in ("frontend", "backend", "supabase", *public_services):
            port = endpoint_port(endpoints, role)
            if port is None:
                print(f"Skipping {role} public route; endpoint port is unavailable")
                continue
            route_name = public_route_name(pr_number, role)
            host = public_route_host(
                pr_number,
                role,
                self.config.public_base_domain,
                self.config.repo_slug,
            )
            url = routes[role] if role in routes else self.public_service_route_url(pr_number, role)
            self.runner.run(
                ["docker", "rm", "-f", route_name],
                check=False,
            )
            self.runner.run(
                [
                    "docker",
                    "run",
                    "-d",
                    "--name",
                    route_name,
                    "--network",
                    "host",
                    "--restart",
                    "unless-stopped",
                    "--label",
                    "traefik.enable=true",
                    "--label",
                    f"traefik.http.routers.{route_name}.entrypoints=websecure",
                    "--label",
                    f"traefik.http.routers.{route_name}.rule=Host(`{host}`)",
                    "--label",
                    (f"traefik.http.routers.{route_name}.tls.certresolver=letsencrypt"),
                    "--label",
                    f"traefik.http.services.{route_name}.loadbalancer.server.port={port}",
                    "--label",
                    (f"traefik.http.services.{route_name}.loadbalancer.passhostheader=false"),
                    self.config.public_route_image,
                    "sleep",
                    "604800",
                ],
            )
            role_payload = ensure_endpoint_payload(updated, role)
            if isinstance(role_payload, dict):
                role_payload["public_url"] = url
                role_payload["public_host"] = host
                role_payload["public_route_container"] = route_name
                health_url = str(role_payload.get("health_url") or "").strip()
                if health_url:
                    health_path = urllib.parse.urlsplit(health_url).path or "/"
                    role_payload["public_health_url"] = f"{url.rstrip('/')}{health_path}"
        return updated

    def remove_public_routes(self, pr_number: int) -> None:
        if not self.config.public_base_domain:
            return
        for role in (
            "frontend",
            "backend",
            "supabase",
            *self.preview_public_services(),
        ):
            self.runner.run(
                ["docker", "rm", "-f", public_route_name(pr_number, role)],
                check=False,
            )

    def stop(
        self,
        pr: PullRequestInfo,
        *,
        reason: str,
        comment: bool,
        release_external_deps: bool = True,
    ) -> int:
        self.remove_public_routes(pr.number)
        state = self.load_state(pr.number)
        project_name = state.project if state and state.project else None
        if not project_name:
            project = self.resolve_project(pr)
            project_name = str(project.get("name")) if project else None
        if not project_name:
            if release_external_deps:
                self.release_external_dependency_leases(
                    pr.number,
                    state.external_dependencies if state else None,
                )
                if state:
                    self.mark_deployment_inactive(state)
                    self.save_state(
                        PreviewState(
                            **{
                                **asdict(state),
                                "status": "stopped",
                                "updated_at": isoformat(utc_now()) or "",
                                "external_dependencies": {},
                            }
                        )
                    )
            if comment:
                self.comment(
                    pr.number,
                    f"{COMMENT_MARKER}\nNo envctl preview project was found for PR #{pr.number}.",
                )
            return 0
        result = self.runner.run(
            [
                self.config.envctl_bin,
                "stop",
                "--trees",
                "--project",
                project_name,
                "--entire-system",
            ],
            cwd=self.config.control_repo,
            check=False,
            env=self.envctl_command_env(),
        )
        status = "stopped" if result.returncode == 0 else "stop_failed"
        external_dependencies = state.external_dependencies if state else None
        if result.returncode == 0:
            self.remove_project_docker_containers(project_name)
        if result.returncode == 0 and state:
            self.mark_deployment_inactive(state)
        if result.returncode == 0 and release_external_deps:
            self.release_external_dependency_leases(
                pr.number,
                external_dependencies,
            )
            external_dependencies = {}
        if state:
            self.save_state(
                PreviewState(
                    **{
                        **asdict(state),
                        "status": status,
                        "updated_at": isoformat(utc_now()) or "",
                        "external_dependencies": external_dependencies,
                    }
                )
            )
        if comment:
            self.comment(
                pr.number,
                self.render_stopped_comment(pr, project_name, reason, result),
            )
        if result.returncode == 0 and release_external_deps and state:
            self.blast_all_if_scaled_to_zero([state], reconcile_states=False)
        return result.returncode

    def delete(self, pr: PullRequestInfo, *, reason: str) -> int:
        stop_code = self.stop(pr, reason=reason, comment=False)
        state = self.load_state(pr.number)
        project_name = state.project if state and state.project else None
        if not project_name:
            project = self.resolve_project(pr)
            project_name = str(project.get("name")) if project else None
        if not project_name:
            self.remove_project_docker_artifacts(pr.head_ref)
            self.remove_imported_branch(pr.head_ref)
            if stop_code == 0:
                self.remove_label(pr.number)
            self.comment(
                pr.number,
                (
                    f"{COMMENT_MARKER}\nNo envctl worktree was found to delete "
                    f"for PR #{pr.number}. Any Docker artifacts matching the "
                    "PR branch slug were cleaned."
                ),
            )
            return stop_code
        delete_result = self.runner.run(
            [self.config.envctl_bin, "delete-worktree", "--project", project_name, "--yes"],
            cwd=self.config.control_repo,
            check=False,
            env=self.envctl_command_env(),
        )
        if delete_result.returncode == 0:
            self.remove_project_docker_artifacts(project_name)
            self.remove_imported_branch(pr.head_ref)
            self.release_external_dependency_leases(pr.number)
            self.state_path(pr.number).unlink(missing_ok=True)
            if stop_code == 0:
                self.remove_label(pr.number)
        self.comment(
            pr.number,
            self.render_deleted_comment(pr, project_name, reason, stop_code, delete_result),
        )
        return delete_result.returncode or stop_code

    def remove_imported_branch(self, branch: str) -> None:
        if not branch:
            return
        self.runner.run(
            ["git", "-C", str(self.config.control_repo), "branch", "-D", branch],
            check=False,
        )

    def sweep(self) -> int:
        active_prs = self.active_prs_with_label()
        if not active_prs:
            return self.blast_all_if_scaled_to_zero()
        now = utc_now()
        exit_code = 0
        for pr in active_prs:
            state = self.load_state(pr.number)
            active_since = self.label_active_since(pr.number)
            saved_active_since = parse_github_datetime(state.label_added_at) if state and state.label_added_at else None
            if active_since is None:
                active_since = saved_active_since
            elif saved_active_since is not None and saved_active_since > active_since:
                active_since = saved_active_since
            if active_since is None:
                active_since = now
            expires_at = active_since + timedelta(minutes=self.config.ttl_minutes)
            if now >= expires_at:
                self.comment(
                    pr.number,
                    self.render_ttl_comment(pr, active_since, expires_at),
                )
                stop_code = self.stop(pr, reason="preview TTL expired", comment=False)
                self.remove_label(pr.number)
                exit_code = exit_code or stop_code
                continue
            if state and state.status in {"running", "starting"}:
                if state.head_sha and state.head_sha != pr.head_sha:
                    exit_code = exit_code or self.start(
                        pr,
                        reason="scheduled reconciliation for new commit",
                    )
                elif state.status == "running":
                    route_health = self.preview_public_route_health(state)
                    if not route_health.ok:
                        print(
                            f"Preview PR #{pr.number} public route is unhealthy; restarting. {route_health.details}",
                            flush=True,
                        )
                        exit_code = exit_code or self.start(
                            pr,
                            reason=("scheduled reconciliation for unhealthy public route"),
                        )
                continue
            if state is None or state.status not in {"running", "starting"}:
                exit_code = exit_code or self.start(pr, reason="scheduled reconciliation")
        return exit_code

    def blast_all_if_scaled_to_zero(
        self,
        candidate_states: list[PreviewState] | None = None,
        reconcile_states: bool = True,
    ) -> int:
        active_prs = self.active_prs_with_label()
        if active_prs:
            return 0
        states = candidate_states or self.load_all_states()
        running_states = [state for state in states if state.status not in {"stopped", "deleted"}]
        if not running_states:
            return 0
        return self.blast_all_preview_processes(
            running_states,
            reconcile_states=reconcile_states,
        )

    def blast_all_preview_processes(
        self,
        states: list[PreviewState],
        *,
        reconcile_states: bool,
    ) -> int:
        for state in states:
            self.remove_public_routes(state.pr_number)
        result = self.runner.run(
            [
                self.config.envctl_bin,
                "blast-all",
                "--force",
                "--blast-keep-worktree-volumes",
                "--blast-keep-main-volumes",
            ],
            cwd=self.config.control_repo,
            check=False,
            env=self.envctl_command_env(),
        )
        if result.returncode != 0:
            return result.returncode
        if not reconcile_states:
            return 0
        updated_at = isoformat(utc_now()) or ""
        for state in states:
            self.mark_deployment_inactive(state)
            self.release_external_dependency_leases(
                state.pr_number,
                state.external_dependencies,
            )
            self.save_state(
                PreviewState(
                    **{
                        **asdict(state),
                        "status": "stopped",
                        "updated_at": updated_at,
                        "external_dependencies": {},
                    }
                )
            )
        return 0

